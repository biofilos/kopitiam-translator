<h1 class="pt-5 text-2xl text-center">Kopitiam translator</h1>
<p class="pt-3 pl-4">
	Use this little app to order your favorite coffee drink (almost) like a local in Singapore. Just
	select how you like your coffee, add your orders, and handy translations will be displayed
</p>
<h2 class="text-xl text-center pt-4">Instructions</h2>
<ol class="list-decimal pl-10">
	<li>
		Select your coffee preferences using the buttons (the current translation is shown above the
		"Add to order" button)
	</li>
	<li>Click on Add to order. Your current orders will be displayed below the button</li>
	<li>Remove orders by tapping on the red circle next to each order</li>
	<li>
		Flipping your phone to landscape orientation will display your order in big letters, in case you
		want to show your order to the kopitiam barista :)
	</li>
</ol>


